package com.data_structure;

public class Hotel {
    private String hotel_no;
    private String hotel_name;
    private String hotel_address;
    private String linkman_telNumber;
    private String postcode;

    public Hotel() {

    }

    public String getHotel_no() {
        return hotel_no;
    }

    public void setHotel_no(String hotel_no) {
        this.hotel_no = hotel_no;
    }

    public String getHotel_name() {
        return hotel_name;
    }

    public void setHotel_name(String hotel_name) {
        this.hotel_name = hotel_name;
    }

    public String getHotel_address() {
        return hotel_address;
    }

    public void setHotel_address(String hotel_address) {
        this.hotel_address = hotel_address;
    }

    public String getLinkman_telNumber() {
        return linkman_telNumber;
    }

    public void setLinkman_telNumber(String linkman_telNumber) {
        this.linkman_telNumber = linkman_telNumber;
    }

    public String getPostcoe() {
        return postcode;
    }

    public void setPostcoe(String postcoe) {
        this.postcode = postcoe;
    }
}
